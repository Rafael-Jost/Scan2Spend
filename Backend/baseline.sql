  CREATE OR REPLACE EDITIONABLE PACKAGE BODY "SCAN2SPEND"."PKG_AUTH" AS

    FUNCTION encrypt_pwd(
        p_password IN VARCHAR2
    ) RETURN VARCHAR2
    IS
    BEGIN
        RETURN LOWER(
            RAWTOHEX(
                DBMS_CRYPTO.HASH(
                    UTL_I18N.STRING_TO_RAW(p_password, 'AL32UTF8'),
                    DBMS_CRYPTO.HASH_SH256
                )
            )
        );
    END;
    
    PROCEDURE auth(
        p_login      IN  VARCHAR2,
        p_senha      IN  VARCHAR2,
        P_usuario_id OUT NUMBER
    ) IS 
        CURSOR c_usuario IS
            SELECT
                usuario_id as id
            FROM
                usuarios
            WHERE 
                UPPER(email) = UPPER(p_login)
            AND senha = encrypt_pwd(p_senha);
            
        r_usuario c_usuario%ROWTYPE;
    BEGIN
        
        OPEN c_usuario;
        FETCH c_usuario INTO r_usuario;
        
        IF c_usuario%FOUND THEN 
            p_usuario_id := r_usuario.id;
        END IF;
        
        CLOSE c_usuario;
    
    EXCEPTION
        WHEN OTHERS THEN 
            raise_application_error(-20001, 'Erro ao validar credenciais de login do usuário');

    END;
    
    PROCEDURE post_auth(
        p_usuario_id IN NUMBER,
        p_nome       OUT VARCHAR2,
        p_sobrenome  OUT VARCHAR2,
        p_email      OUT VARChAR2
    ) IS 
        CURSOR c_usuario IS
            SELECT
                nome,
                sobrenome,
                email
            FROM
                usuarios
            WHERE 
                usuario_id = p_usuario_id;
        
        r_usuario c_usuario%ROWTYPE;
        
    BEGIN
        
        OPEN c_usuario;
        FETCH c_usuario INTO r_usuario;
        
        IF c_usuario%FOUND THEN
            p_nome := r_usuario.nome;
            p_sobrenome := r_usuario.sobrenome;
            p_email := r_usuario.email;
        END IF;
        
        CLOSE c_usuario;
    
    EXCEPTION
        WHEN OTHERS THEN 
            raise_application_error(-20001, 'Erro ao retornar informaçőes do usuário');
    
    END;

END pkg_auth;

